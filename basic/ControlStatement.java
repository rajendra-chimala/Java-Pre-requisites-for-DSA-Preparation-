
public class ControlStatement {

    public static void main(String[] args) {
        int age = 10;
        if (age < 18) {
            System.out.println("You are a minor");
        } else if (age == 18) {
            System.out.println("You are a teenager");
        } else {
            System.out.println("You are an adult");
        }
    }

}
